# aplikasi yang mengikuti perkembangan zaman yaitu Gojek
# aplikasi gojek merupakan salah satu aplikasi yang saat ini tidak bisa lepas dari kehidupan masyarakat indonesia.
# aplikasi ini diciptakan awalnya untuk membantu proses pemesanan ojek secara online dan saat ini telah berkembang
# dengan banyaknya fitur-fitur seperti beli pulsa, pesan makanan (GoFood), bahkan delivery barang.
# untuk proses pembayarannya, dulu hanya bisa dilakukan cash tetapi seiring dengan hitsnya e-money, aplikasi ini
# mendukung pemayaran melalui OVO, GoPay, dan emoney lainnya.
